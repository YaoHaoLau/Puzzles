class Solution {
public:
    virtual int findMinArrowShots(vector<vector<int>>& points) = 0;
};

class SolutionCppA : public Solution {
    int findMinArrowShots(vector<vector<int>>& points) {
        sort(points.begin(), points.end(), [](const vector<int> &a, const vector<int> &b) {
            return a[1] < b[1];
        });

        int arrow_num = 0;
        while (!points.empty()) {
            auto iter = points.begin();
            int reach_max = (*iter)[1];
            iter = points.erase(iter);

            while (iter != points.end()) {
                if ((*iter)[0] <= reach_max) {
                    iter = points.erase(iter);
                } else {
                    break;
                }
            }
            ++ arrow_num;
        }

        return arrow_num;
    }
};

class SolutionCppB : public Solution {
    int findMinArrowShots(vector<vector<int>>& points) {
        sort(points.begin(), points.end(), [](const vector<int> &a, const vector<int> &b) {
            return a[1] < b[1];
        });

        int arrow_num = 0;
        auto iter = points.begin();
        while (iter != points.end()) {
            int reach_max = (*iter)[1];
            iter ++;

            while (iter != points.end()) {
                if ((*iter)[0] <= reach_max) {
                    ++ iter;
                } else {
                    break;
                }
            }
            ++ arrow_num;
        }

        return arrow_num;
    }
};

class SolutionCppC : public Solution {
    int findMinArrowShots(vector<vector<int>>& points) {
        sort(points.begin(), points.end(), [](const vector<int> &a, const vector<int> &b) {
            return a[1] < b[1];
        });

        int arrow_num = 1;
        auto iter = points.begin();
        int reach_max = (*iter)[1];
        iter ++;

        while (iter != points.end()) {
            if ((*iter)[0] > reach_max) {
                ++ arrow_num;
                reach_max = (*iter)[1];
            }
            ++ iter;
        }

        return arrow_num;
    }
};

class SolutionCppD : public Solution {
    int findMinArrowShots(vector<vector<int>>& points) {
        if (points.size() < 2) {
            return 1;
        }
        sort(points.begin(), points.end(), [](const vector<int> &a, const vector<int> &b) {
            return a[1] < b[1];
        });

        int arrow_num = 1;
        int reach_max = points[0][1];

        for (int inx = 1; inx < points.size(); ++ inx) {
            if (points[inx][0] > reach_max) {
                ++ arrow_num;
                reach_max = points[inx][1];
            }
        }

        return arrow_num;
    }
};

class SolutionCppE : public Solution {
    int findMinArrowShots(vector<vector<int>>& points) {
        vector<pair<int, int>> pairs;
        for (const auto& point : points) {
            pairs.emplace_back(std::make_pair(point[0], point[1]));
        }
        sort(pairs.begin(), pairs.end(), [](const pair<int, int> &a, const pair<int, int> &b) {
            return a.second < b.second;
            // return std::tie(a.second, a.first) < std::tie(b.second, b.first);
        });

        int arrow_num = 1;
        int reach_max = pairs[0].second;

        for (int inx = 1; inx < pairs.size(); ++ inx) {
            if (pairs[inx].first > reach_max) {
                ++ arrow_num;
                reach_max = pairs[inx].second;
            }
        }

        return arrow_num;
    }
};

class SolutionCppF : public Solution {
    int findMinArrowShots(vector<vector<int>>& points) {
        vector<pair<int, int>> pairs;
        for (const auto& point : points) {
            pairs.emplace_back(std::make_pair(point[0], point[1]));
        }
        sort(pairs.begin(), pairs.end(), [](const pair<int, int> &a, const pair<int, int> &b) {
            return a.second < b.second;
            // return std::tie(a.second, a.first) < std::tie(b.second, b.first);
        });

        int arrow_num = 1;
        int reach_max = pairs[0].second;

        for (int inx = 1; inx < pairs.size(); ++ inx) {
            if (pairs[inx].first > reach_max) {
                ++ arrow_num;
                reach_max = pairs[inx].second;
            } else if (pairs[inx].second < reach_max) {
                reach_max = pairs[inx].second;
            }
        }

        return arrow_num;
    }
};

